
"use client"

import { Button } from "@/components/ui/button";
import { toast } from "sonner";

export function SonnerToast({
  variant,
  title,
  description,
  label,
  consoleLog,
}: SonnerToastProps) {
  return (
    <Button
      variant={variant}
      onClick={() =>
        toast(title, {
          description: description,
          action: {
            label: label,
            onClick: () => console.log(consoleLog),
          },
        })
      }
    >
    </Button>
  )
};
